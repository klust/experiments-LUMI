help( [==[
This is just a test module that defines the lumi-gather-info shell function
to gather information from the user's shell for LUST.
]==] )


whatis( 'Description: Test module that defines the lumi-gather-info shell function.' )

bash_gather_info = string.gsub( [==[
    #
    # Print a message to stderr explaining the purpose of the function.
    # 
    echo "You are free to share all or only part of the info generated by this command"       1>&2 ;
    echo "with the LUMI User Support Team (LUST). However, LUST can refuse to offer"          1>&2 ;
    echo "support if the information they receive is deemed insufficient if you don't want"   1>&2 ;
    echo "to share all information generated by this command."                                1>&2 ;
    echo "Rather than copying from the terminal, you can also redirect the output to a"       1>&2 ;
    echo "file (using >), copy that file to your home machine and attach it to the ticket"    1>&2 ;
    echo "or mail. This message will still appear on your screen."                            1>&2 ;
    
    #
    # Print some general info about the user and their projects
    #   
    echo -e "\nGeneral info\n------------\n" ;
    echo "User: $(whoami), \$USER: $USER" ;
    echo "Groups: $(groups)" ;
    echo "Running on $HOSTNAME (hostname -A is $(hostname -A))" ;
    echo "SHELL environment variable: $SHELL (not a robust way to detect the current shell though)" ;
    echo "Default shell (from passwd): $(getent passwd | awk -F: -v user="$(whoami)" '$1 == user {print $NF}')" ;

    local _detected_shell='' ;
    if [ -n "$BASH_VERSION" ] ;
    then
        _detected_shell='bash' ;
        echo "User is running in a bash shell (bash $BASH_VERSION)" ;
    elif [ -n "$ZSH_VERSION" ] ;
    then 
        _detected_shell='zsh' ;
        echo "WARNING User is running in a zsh shell (zsh $ZSH_VERSION). Note that zsh is unsupported on LUMI." ;
    else
        _detected_shell='unknown' ;
        echo "WARNING Failed to detect the current user shell, but it is not bash or zsh unless the environment is corrupt." ;
    fi ;
    
    /usr/bin/diff -q ~/.profile /etc/skel/.profile >/dev/null ;
    if [ $? -eq 0 ] ;
    then
        echo "The user .profile file is the same as /etc/skel/.profile" ;
    else
        echo "NOTE: The user .profile file differs from /etc/skel/.profile" ;
    fi ;
    
    /usr/bin/diff -q ~/.bashrc /etc/skel/.bashrc >/dev/null ;
    if [ $? -eq 0 ] ;
    then
        echo "The user .bashrc file is the same as /etc/skel/.bashrc" ;
    else
        echo "NORE: The user .bashrc file differs from /etc/skel/.bashrc" ;
    fi ;
    
    echo "File systems:" ;
    echo "- Home directory location: $(/bin/ls -l $HOME | /usr/bin/sed -e 's/^.*-> //')" ;
    for project in $(groups | tr ' ' '\n' | egrep '^project_4') ;
    do
        echo "- /project/$project location: $(/bin/ls -l /project/$project | /usr/bin/sed -e 's/^.*-> //')" ;
        echo "- /scratch/$project location: $(/bin/ls -l /scratch/$project | /usr/bin/sed -e 's/^.*-> //')" ;
    done ;    
    
    echo "Quota use:" ;
    local _home_quota=($(lfs quota -q -p $(( 1000000000 + $(id -u $(whoami)) )) $HOME 2>/dev/null)) ;
    printf -- "- Used quota on $HOME: block %s%% (%s/%s/%s), files %s%% (%s/%s/%s)\n"
        $(( ${_home_quota[@]:1:1}*100/${_home_quota[@]:2:1} ))
        $(/usr/bin/numfmt  --to si --from iec ${_home_quota[@]:1:1}) $(/usr/bin/numfmt  --to si --from iec ${_home_quota[@]:2:1}) $(/usr/bin/numfmt  --to si --from iec ${_home_quota[@]:3:1})
        $(( ${_home_quota[@]:5:1}*100/${_home_quota[@]:6:1} ))
        $(/usr/bin/numfmt  --to si --from iec ${_home_quota[@]:5:1}) $(/usr/bin/numfmt  --to si --from iec ${_home_quota[@]:6:1}) $(/usr/bin/numfmt  --to si --from iec ${_home_quota[@]:7:1}) ;
    
    for project in $(groups | tr ' ' '\n' | egrep '^project_4') ;
    do
        local gid=$(getent group $project | cut -d : -f3) ;
        local _project_quota=($(lfs quota -q -p $(( 2000000000 + ${gid} )) /project/$project)) ;
        local _scratch_quota=($(lfs quota -q -p $(( 3000000000 + ${gid} )) /scratch/$project)) ;
        printf -- "- Used quota for %s on /project: block %s%% (%s/%s/%s), files %s%% (%s/%s/%s)\n" 
               $project 
               $(( ${_project_quota[@]:1:1}*100/${_project_quota[@]:2:1} )) 
               $(/usr/bin/numfmt  --to si --from iec ${_project_quota[@]:1:1}) $(/usr/bin/numfmt  --to si --from iec ${_project_quota[@]:2:1}) $(/usr/bin/numfmt  --to si --from iec ${_project_quota[@]:3:1}) 
               $(( ${_project_quota[@]:5:1}*100/${_project_quota[@]:6:1} )) 
               $(/usr/bin/numfmt  --to si --from iec ${_project_quota[@]:5:1}) $(/usr/bin/numfmt  --to si --from iec ${_project_quota[@]:6:1}) $(/usr/bin/numfmt  --to si --from iec ${_project_quota[@]:7:1}) ;
        printf -- "- Used quota for %s on /scratch: block %s%% (%s/%s/%s), files %s%% (%s/%s/%s)\n" 
               $project 
               $(( ${_scratch_quota[@]:1:1}*100/${_scratch_quota[@]:2:1} )) 
               $(/usr/bin/numfmt  --to si --from iec ${_scratch_quota[@]:1:1}) $(/usr/bin/numfmt  --to si --from iec ${_scratch_quota[@]:2:1}) $(/usr/bin/numfmt  --to si --from iec ${_scratch_quota[@]:3:1}) 
               $(( ${_scratch_quota[@]:5:1}*100/${_scratch_quota[@]:6:1} )) 
               $(/usr/bin/numfmt  --to si --from iec ${_scratch_quota[@]:5:1}) $(/usr/bin/numfmt  --to si --from iec ${_scratch_quota[@]:6:1}) $(/usr/bin/numfmt  --to si --from iec ${_scratch_quota[@]:7:1}) ;
    done ;
    
    #
    # Part 2: Info about modules
    #
    echo -e "\nModule info\n----------\n" ;
    
    echo -e "MODULEPATH:" ;
    echo $MODULEPATH | tr ":" "\n" | nl - ;
    
    echo -e "\nLoaded module and their module files:" ;
    for mod in $(module -t list 2>&1) ;
    do 
        echo "- $mod from $(module show $mod |& /usr/bin/sed -n '2p' | /usr/bin/sed -e 's/://' -e 's/^ *//')" ;
    done ;
    
    #
    # Part 3: Info about the software stack configuration
    # Not all tests work with zsh as the output of declare is different in zsh. Not tested whis ksh.
    #
    echo -e "\nLUMI software stack configuration\n---------------------------------\n" ;
    
    if [[ -v EBU_USER_PREFIX ]] ; # Test checked with both bash and zsh.
    then
        # EBU_USER_PREFIX is defined, now check its value and if it is exported.
        if [ -z "${EBU_USER_PREFIX}" ] ;
        then
            echo -e "EBU_USER_PREFIX is defined but empty (turning off the user software stack)" ;
        else
            echo -e "EBU_USER_PREFIX is defined, its value is $EBU_USER_PREFIX" ;
        fi ;
        # Check if the variable is exported
        if [[ $_detected_shell == 'bash' ]] ;
        then
            if ( declare -p EBU_USER_PREFIX | /usr/bin/egrep -q ' \-x ' ) ;
            then
                echo "OK EBU_USER_PREFIX is exported (as it should)" ;
            else
                echo "NOT OK EBU_USER_PREFIX is not exported hence will not be picked up by the module system" ;
            fi ;
        elif [[ $_detected_shell == 'zsh' ]] ;
        then
            if ( declare -p EBU_USER_PREFIX | /usr/bin/egrep -q '^export' ) ;
            then
                echo "OK EBU_USER_PREFIX is exported (as it should)" ;
            else
                echo "NOT OK EBU_USER_PREFIX is not exported hence will not be picked up by the module system" ;
            fi ;
        else
            echo "Don't know how to check if EBU_USER_PREFIX is exported." ;
        fi;
    else
        echo "EBU_USER_PREFIX is NOT defined" ;
    fi ;

    echo -e "\nLMOD configuration (LMOD_ environment variables without LMOD_FAMILY):" ;
    /usr/bin/env | /usr/bin/egrep "^LMOD" | /usr/bin/egrep -v "LMOD_FAMILY" | /usr/bin/sort | /usr/bin/sed -e 's/^/\ \ \ \ /' ;

    #
    # Part 4: Other path-style variables
    #
    echo -e "\nOther path-style variables\n--------------------------\n" ;

    echo -e "PATH:" ;
    echo $PATH | tr ":" "\n" | nl - ;
    echo -e "\nLD_LIBRARY_PATH:" ;
    echo $LD_LIBRARY_PATH | tr ":" "\n" | nl - ;
    echo -e "\nCRAY_LD_LIBRARY_PATH:" ;
    echo $CRAY_LD_LIBRARY_PATH | tr ":" "\n" | nl - ;
    
    echo ; 

]==], '#[^\n]*', '')




set_shell_function( 'lumi-gather-info', bash_gather_info, 'echo "Not implemented."' )
